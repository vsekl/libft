#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*str;
	size_t	len;

	len = ft_strlen(s) + 1;
	str = malloc(sizeof(char) * len);
	if (str == 0)
		return (NULL);
	str = ft_memcpy(str, s, len);
	return (str);
}
